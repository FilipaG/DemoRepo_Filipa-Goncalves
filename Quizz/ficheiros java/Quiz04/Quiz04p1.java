
public class Quiz04p1 {

	public static void main(String[] args) {
		        
		        System.out.println(16 >>> 3); // >>>converte 16 para binário (10000) e "empurra/faz desaparecer" os 3 últimos e preenche algarismos (10) e converte o resultado para número inteiro novamente
		        System.out.println(19 >>> 3);
		        System.out.println(-20 >>> 3);
		        System.out.println(-20 >> 3);
		        System.out.println(Integer.toBinaryString(-20));
		       	}
		
}
